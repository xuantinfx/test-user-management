import { VueQueryPlugin, QueryClient } from '@tanstack/vue-query';

// Create a client
const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      retry: 1,
      staleTime: 5 * 60 * 1000, // 5 minutes
    },
  },
});

export { queryClient };
export default VueQueryPlugin;
